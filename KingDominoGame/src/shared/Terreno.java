package shared;

public class Terreno {
	
	private String codigoColor;
	private String pathTerreno;
	
	public Terreno(String codigoColor, String pathTerreno) {

		this.codigoColor = codigoColor;
		this.pathTerreno = pathTerreno;
	}

	public String getCodigoColor() {
		return codigoColor;
	}

	public void setCodigoColor(String codigoColor) {
		this.codigoColor = codigoColor;
	}

	public String getPathTerreno() {
		return pathTerreno;
	}

	public void setPathTerreno(String pathTerreno) {
		this.pathTerreno = pathTerreno;
	}
	
	
	
	
	

}
